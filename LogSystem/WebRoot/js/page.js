
	function goPage(page) {
		document.frm.curPage.value = page;
		document.frm.submit();
	}

	
	function prePage() {
		document.frm.curPage.value = parseInt(document.frm.curPage.value) - 1;
		document.frm.submit();
	}

	
	function nextPage() {
		
		document.frm.curPage.value = parseInt(document.frm.curPage.value) + 1;
		document.frm.submit();
	}

	
	function query() {

		document.frm.curPage.value = 1;
		document.frm.submit();
	}